"""El método swapcase invierte todas las letras de una cadena de carácteres. Si
estan en minúsculas pasan a mayúsculas y si estan en mayúsculas pasan a minúsculas"""

txt_lower_upper = "mUNDO dE mINECRAFT"
print (txt_lower_upper.swapcase()) 


txt_upper = "CADENA DE MAYÚSCULAS"
print (txt_upper.swapcase())

txt_lower = "cadena con minúsculas"
print (txt_lower.swapcase())

