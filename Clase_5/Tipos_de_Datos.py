#Tipo de dato Entero o largo
numero = 15
print (numero, type (numero))

#tipo de dato Flotante o Reales (estos números tinen una parte decimal)
numero_flotante = 15.5
print (numero_flotante, type (numero_flotante))

"""tipo de dato Número Complejo (estos números se separan por su parte real
y su parte imaginaria, en este caso el número 5 es la parte real y el 6j la parte
imaginaria"""
numero_complejo = 5 + 6j
print (numero_complejo, type (numero_complejo))

#tipo de dato String o Cadena de arácteres
nombre = "minecraft"
print (nombre, type (nombre))

#tipo de dato Booleano
verdadero_falso = 3 == 3
print (verdadero_falso, type (verdadero_falso))