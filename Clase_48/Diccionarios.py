# Diccionario vacío

Diccionario = {}

print (f"Diccionario vacío: {Diccionario}\n")

# Diccionario homogéneo

Diccionario_de_edades = {"Alex": 32,
						 "Jhonson": 21,
						 "Cesar": 25
						 }

print (f"Diccionario de edades: {Diccionario_de_edades}\n")

# Diccionario heterogéneo

Diccionario_de_datos = {"name": "Carmen",
						"last_name": "Flores",
						"age": 34
						}

print (f"Diccionario de datos: {Diccionario_de_datos}\n")

# Un diccionario puede almacenar listas y diccionarios

Diccionario_listas = {"a": [1, 2, 3], "b": {"a": 1, "b": 2, "c":3}}

print (f"Diccionario de listas y Diccionarios: {Diccionario_listas}\n")

# Un diccionario puede tener claves numéricas

Diccionario_de_claves_numéricas = {4:1,
         						   5:2,
         						   6:3
         						      }

print (f"Diccionario de claves numéricas: {Diccionario_de_claves_numéricas}\n")

